print("Fatiamento de string é uma técnica utilizada para retornar substrings(partes da string original), informando inicio, fim e passo [start: stop[, step]].")

nome = "Carlos Henrique Pavão Inacio"
print(nome)

print(nome[0])

print(nome[:9])

print(nome[10:])

print(nome[10:16])

print(nome[10:16:2])

print(nome[:])

print(nome[:: -1])